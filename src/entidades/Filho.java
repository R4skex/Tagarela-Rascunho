package entidades;

public class Filho {
    public String nome;
    public String genero;
    public String aniversario;
    public String tipoSanguineo;
    public String alergia;

    public Filho(String nome, String genero, String aniversario, String tipoSanguineo, String alergia) {
        this.nome = nome;
        this.genero = genero;
        this.aniversario = aniversario;
        this.tipoSanguineo = tipoSanguineo;
        this.alergia = alergia;
    }
}
